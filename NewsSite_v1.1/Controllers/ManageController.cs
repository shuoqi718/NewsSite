        public ActionResult Upload()
        {
            ArrayList fileList = new ArrayList();
            string path = "~/Upload/";
            DirectoryInfo dir = new DirectoryInfo(Server.MapPath(path));
            try
            {
                if (!Directory.Exists(path))
                {
                    Directory.CreateDirectory(path);
                }
            }
            catch(Exception e)
            {
                ViewBag.exception = "There is no such file";
            }
            
            foreach (FileInfo file in dir.GetFiles())
            {
                if (file.Extension == ".jpg" ||
                    file.Extension == ".jpeg" ||
                    file.Extension == ".bmp" ||
                    file.Extension == "gif" ||
                    file.Extension == "png")
                {
                    fileList.Add(path + file.Name);
                }
            }
            ViewBag.fileList = fileList;
            return View();
        }

        [HttpPost]
        [Authorize]
        public ActionResult Upload(HttpPostedFileBase postedFile)
        {
            if (postedFile != null)
            {
                string path = Server.MapPath("~/Upload/");
                if (!Directory.Exists(path))
                {
                    Directory.CreateDirectory(path);
                }
                FileInfo file = new FileInfo(postedFile.FileName);
                if(file.Extension == ".jpg" ||
                    file.Extension == ".jpeg" ||
                    file.Extension == ".bmp" ||
                    file.Extension == "gif" ||
                    file.Extension == "png")
                {
                    postedFile.SaveAs(path + Path.GetFileName(postedFile.FileName));
                    ViewBag.Message = "File uploaded successfully.";
                }
                else
                {
                    ViewBag.Message = "Sorry, you can't upload this type of file";
                }

                
            }

            return View();
        }

       
        
